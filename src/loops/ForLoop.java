package loops;

public class ForLoop {
    public static void main(String[] args) {
        //syntax
//        for(initialization;condition;increment/decrement){
//            statement(s);
//        }
            //Ascending order
        for(int i=1;i<=10;i++){
            System.out.println(i);
        }
        //Descending order
        for(int j=10;j>0;j--){
            System.out.println(j);
        }
    }
}
